"""Aqua-D4RT synthetic transient clip dataset.

This adapter reads clips generated by `scripts/build_aqua_synthetic_transients.py`
and exposes query-level fish/particle labels for transient-head training.
"""

from __future__ import annotations

import json
from dataclasses import dataclass
from pathlib import Path
from typing import Any

import cv2
import numpy as np
import torch
from torch.utils.data import Dataset

from .seeding import SeededDatasetMixin


@dataclass
class AquaSynthTransientConfig:
    manifests: tuple[Path, ...]
    split: str
    clip_frames: int
    image_size: tuple[int, int]
    queries_per_clip: int
    repeat_per_epoch: int = 128
    dynamic_query_fraction: float = 0.35
    particle_query_fraction: float = 0.25
    boundary_query_fraction: float = 0.0
    small_particle_query_fraction: float = 0.0
    static_hard_negative_fraction: float = 0.0
    boundary_dilate_px: int = 2
    small_particle_max_area_px: int = 64
    hard_negative_edge_quantile: float = 0.85
    same_timestep_queries: bool = True


def _to_path_list(value: Any) -> tuple[Path, ...]:
    if value is None:
        return tuple()
    if isinstance(value, str):
        parts = [part.strip() for part in value.split(",") if part.strip()]
        return tuple(Path(part) for part in parts)
    if isinstance(value, (list, tuple)):
        return tuple(Path(str(part)) for part in value if str(part).strip())
    return tuple()


def _to_paths_from_manifest_lists(value: Any) -> tuple[Path, ...]:
    out: list[Path] = []
    for list_path in _to_path_list(value):
        if not list_path.exists():
            raise FileNotFoundError(f"Aqua manifest list not found: {list_path}")
        for line in list_path.read_text(encoding="utf-8").splitlines():
            item = line.strip()
            if item and not item.startswith("#"):
                out.append(Path(item))
    return tuple(out)


def aqua_synth_cfg_from_train_config(cfg: Any, *, split: str, manifest_paths: list[str] | None = None) -> AquaSynthTransientConfig:
    image_size_cfg = cfg.get_path("data.image_size", [256, 256])
    image_size = (int(image_size_cfg[0]), int(image_size_cfg[1]))
    source = tuple(Path(item) for item in manifest_paths or [])
    if not source:
        source = _to_path_list(cfg.get_path(f"data.aqua_synth.{split}_manifests", None))
    if not source:
        source = _to_paths_from_manifest_lists(
            cfg.get_path(
                f"data.aqua_synth.{split}_manifest_lists",
                cfg.get_path(f"data.aqua_synth.{split}_manifest_list", None),
            )
        )
    if not source:
        source = _to_path_list(cfg.get_path("data.aqua_synth.manifests", None))
    if not source:
        source = _to_paths_from_manifest_lists(
            cfg.get_path("data.aqua_synth.manifest_lists", cfg.get_path("data.aqua_synth.manifest_list", None))
        )
    if not source:
        raise ValueError("No Aqua synthetic manifests configured.")

    return AquaSynthTransientConfig(
        manifests=source,
        split=str(split),
        clip_frames=int(cfg.get_path("data.clip_frames", 32)),
        image_size=image_size,
        queries_per_clip=int(cfg.get_path("train_sampling.queries_per_clip", 1024)),
        repeat_per_epoch=int(cfg.get_path("data.aqua_synth.repeat_per_epoch", 128)),
        dynamic_query_fraction=float(cfg.get_path("data.aqua_synth.dynamic_query_fraction", 0.35)),
        particle_query_fraction=float(cfg.get_path("data.aqua_synth.particle_query_fraction", 0.25)),
        boundary_query_fraction=float(cfg.get_path("data.aqua_synth.boundary_query_fraction", 0.0)),
        small_particle_query_fraction=float(cfg.get_path("data.aqua_synth.small_particle_query_fraction", 0.0)),
        static_hard_negative_fraction=float(cfg.get_path("data.aqua_synth.static_hard_negative_fraction", 0.0)),
        boundary_dilate_px=int(cfg.get_path("data.aqua_synth.boundary_dilate_px", 2)),
        small_particle_max_area_px=int(cfg.get_path("data.aqua_synth.small_particle_max_area_px", 64)),
        hard_negative_edge_quantile=float(cfg.get_path("data.aqua_synth.hard_negative_edge_quantile", 0.85)),
        same_timestep_queries=bool(cfg.get_path("data.aqua_synth.same_timestep_queries", True)),
    )


def _read_rgb(path: str | Path, image_size: tuple[int, int]) -> np.ndarray:
    bgr = cv2.imread(str(path), cv2.IMREAD_COLOR)
    if bgr is None:
        raise RuntimeError(f"Failed to read frame: {path}")
    h, w = image_size
    if bgr.shape[:2] != (h, w):
        bgr = cv2.resize(bgr, (w, h), interpolation=cv2.INTER_AREA)
    return cv2.cvtColor(bgr, cv2.COLOR_BGR2RGB)


def _resize_mask(mask: np.ndarray, image_size: tuple[int, int]) -> np.ndarray:
    h, w = image_size
    if mask.shape[:2] != (h, w):
        mask = cv2.resize(mask.astype(np.uint8), (w, h), interpolation=cv2.INTER_NEAREST)
    return mask.astype(bool)


def _sample_from_mask(
    mask_t_hw: np.ndarray,
    count: int,
    rng: np.random.Generator,
) -> np.ndarray:
    coords = np.argwhere(mask_t_hw)
    if coords.size == 0 or count <= 0:
        return np.empty((0, 3), dtype=np.int64)
    pick = rng.integers(0, coords.shape[0], size=int(count))
    return coords[pick].astype(np.int64)


def _morphological_boundary(mask_t_hw: np.ndarray, radius_px: int) -> np.ndarray:
    """Return a narrow band around binary mask boundaries for query sampling."""

    radius_px = max(0, int(radius_px))
    if radius_px <= 0 or not bool(mask_t_hw.any()):
        return np.zeros_like(mask_t_hw, dtype=bool)
    kernel_size = 2 * radius_px + 1
    kernel = np.ones((kernel_size, kernel_size), dtype=np.uint8)
    out = np.zeros_like(mask_t_hw, dtype=bool)
    for frame_idx in range(mask_t_hw.shape[0]):
        frame = mask_t_hw[frame_idx].astype(np.uint8)
        if frame.max() == 0:
            continue
        dilated = cv2.dilate(frame, kernel, iterations=1).astype(bool)
        eroded = cv2.erode(frame, kernel, iterations=1).astype(bool)
        out[frame_idx] = dilated & ~eroded
    return out


def _small_connected_components(mask_t_hw: np.ndarray, max_area_px: int) -> np.ndarray:
    max_area_px = max(1, int(max_area_px))
    if not bool(mask_t_hw.any()):
        return np.zeros_like(mask_t_hw, dtype=bool)
    out = np.zeros_like(mask_t_hw, dtype=bool)
    for frame_idx in range(mask_t_hw.shape[0]):
        frame = mask_t_hw[frame_idx].astype(np.uint8)
        if frame.max() == 0:
            continue
        num_labels, labels, stats, _ = cv2.connectedComponentsWithStats(frame, connectivity=8)
        if num_labels <= 1:
            continue
        areas = stats[1:, cv2.CC_STAT_AREA]
        small_ids = np.nonzero(areas <= max_area_px)[0] + 1
        if small_ids.size > 0:
            out[frame_idx] = np.isin(labels, small_ids)
    return out


def _static_hard_negative_mask(
    video_rgb_t_hw3: np.ndarray,
    static_mask_t_hw: np.ndarray,
    quantile: float,
) -> np.ndarray:
    if not bool(static_mask_t_hw.any()):
        return np.zeros_like(static_mask_t_hw, dtype=bool)
    q = float(np.clip(quantile, 0.0, 1.0))
    out = np.zeros_like(static_mask_t_hw, dtype=bool)
    for frame_idx in range(video_rgb_t_hw3.shape[0]):
        static = static_mask_t_hw[frame_idx]
        if not bool(static.any()):
            continue
        gray = cv2.cvtColor(video_rgb_t_hw3[frame_idx], cv2.COLOR_RGB2GRAY).astype(np.float32) / 255.0
        grad_x = cv2.Sobel(gray, cv2.CV_32F, 1, 0, ksize=3)
        grad_y = cv2.Sobel(gray, cv2.CV_32F, 0, 1, ksize=3)
        edge_mag = np.sqrt(grad_x * grad_x + grad_y * grad_y)
        static_values = edge_mag[static]
        if static_values.size == 0:
            continue
        threshold = float(np.quantile(static_values, q))
        out[frame_idx] = static & (edge_mag >= threshold)
    return out


class AquaSynthTransientDataset(SeededDatasetMixin, Dataset):
    """Loads synthetic corrupted clips and samples balanced transient queries."""

    def __init__(self, config: AquaSynthTransientConfig) -> None:
        self.cfg = config
        self.manifests = [Path(path) for path in config.manifests]
        self._manifest_payloads = [json.loads(path.read_text(encoding="utf-8")) for path in self.manifests]
        self._init_dataset_seeding(namespace=f"aqua_synth_transient::{config.split}", default_seed=20260610)

    def __len__(self) -> int:
        return max(1, len(self.manifests) * max(1, int(self.cfg.repeat_per_epoch)))

    def _clip_payload(self, index: int) -> tuple[Path, dict[str, Any]]:
        clip_idx = int(index) % len(self.manifests)
        return self.manifests[clip_idx], self._manifest_payloads[clip_idx]

    def _load_clip(self, payload: dict[str, Any]) -> tuple[np.ndarray, np.ndarray, np.ndarray, float]:
        frame_paths = [str(path) for path in payload["frames"]]
        frame_paths = frame_paths[: max(1, int(self.cfg.clip_frames))]
        frames = [_read_rgb(path, self.cfg.image_size) for path in frame_paths]
        video = np.stack(frames, axis=0)

        labels_path = Path(str(payload["labels_npz"]))
        masks = np.load(labels_path)
        dynamic = masks["dynamic_object_mask"][: len(frames)]
        particle = masks["particle_mask"][: len(frames)]
        dynamic = np.stack([_resize_mask(dynamic[t], self.cfg.image_size) for t in range(dynamic.shape[0])], axis=0)
        particle = np.stack([_resize_mask(particle[t], self.cfg.image_size) for t in range(particle.shape[0])], axis=0)

        source_w = float(payload.get("width", video.shape[2]))
        source_h = float(payload.get("height", video.shape[1]))
        aspect_ratio = source_w / max(source_h, 1.0)
        return video, dynamic, particle, aspect_ratio

    def _sample_queries(
        self,
        video_rgb: np.ndarray,
        dynamic: np.ndarray,
        particle: np.ndarray,
        rng: np.random.Generator,
    ) -> dict[str, np.ndarray]:
        t, h, w = dynamic.shape
        m = max(1, int(self.cfg.queries_per_clip))
        fractions = {
            "boundary": float(np.clip(self.cfg.boundary_query_fraction, 0.0, 1.0)),
            "small_particle": float(np.clip(self.cfg.small_particle_query_fraction, 0.0, 1.0)),
            "hard_static": float(np.clip(self.cfg.static_hard_negative_fraction, 0.0, 1.0)),
            "dynamic": float(np.clip(self.cfg.dynamic_query_fraction, 0.0, 1.0)),
            "particle": float(np.clip(self.cfg.particle_query_fraction, 0.0, 1.0)),
        }
        counts: dict[str, int] = {}
        remaining = m
        for name, fraction in fractions.items():
            count = min(remaining, max(0, int(round(m * fraction))))
            counts[name] = count
            remaining -= count
        counts["background"] = remaining

        transient = dynamic | particle
        static = ~transient
        boundary = (
            _morphological_boundary(transient, self.cfg.boundary_dilate_px)
            if counts["boundary"] > 0
            else np.zeros_like(transient, dtype=bool)
        )
        small_particle = (
            _small_connected_components(particle & ~dynamic, self.cfg.small_particle_max_area_px)
            if counts["small_particle"] > 0
            else np.zeros_like(transient, dtype=bool)
        )
        hard_static = (
            _static_hard_negative_mask(
                video_rgb_t_hw3=video_rgb,
                static_mask_t_hw=static,
                quantile=self.cfg.hard_negative_edge_quantile,
            )
            if counts["hard_static"] > 0
            else np.zeros_like(transient, dtype=bool)
        )

        sampled_by_type = [
            ("boundary", _sample_from_mask(boundary, counts["boundary"], rng)),
            ("small_particle", _sample_from_mask(small_particle, counts["small_particle"], rng)),
            ("hard_static", _sample_from_mask(hard_static, counts["hard_static"], rng)),
            ("dynamic", _sample_from_mask(dynamic, counts["dynamic"], rng)),
            ("particle", _sample_from_mask(particle & ~dynamic, counts["particle"], rng)),
            ("background", _sample_from_mask(static, counts["background"], rng)),
        ]

        coords_parts: list[np.ndarray] = []
        source_parts: dict[str, list[np.ndarray]] = {name: [] for name, _ in sampled_by_type}
        for name, arr in sampled_by_type:
            if arr.size == 0:
                continue
            coords_parts.append(arr)
            for key in source_parts:
                source_parts[key].append(np.full((arr.shape[0],), key == name, dtype=bool))

        if coords_parts:
            coords = np.concatenate(coords_parts, axis=0)
            source_flags = {
                key: np.concatenate(parts, axis=0) if parts else np.empty((0,), dtype=bool)
                for key, parts in source_parts.items()
            }
        else:
            coords = np.empty((0, 3), dtype=np.int64)
            source_flags = {key: np.empty((0,), dtype=bool) for key in source_parts}

        if coords.shape[0] < m:
            missing = m - coords.shape[0]
            fallback = np.column_stack(
                [
                    rng.integers(0, t, size=missing),
                    rng.integers(0, h, size=missing),
                    rng.integers(0, w, size=missing),
                ]
            ).astype(np.int64)
            coords = np.concatenate([coords, fallback], axis=0)
            for key in source_flags:
                source_flags[key] = np.concatenate([source_flags[key], np.zeros((missing,), dtype=bool)], axis=0)
        if coords.shape[0] > m:
            coords = coords[:m]
            for key in source_flags:
                source_flags[key] = source_flags[key][:m]
        order = rng.permutation(coords.shape[0])
        coords = coords[order]
        source_flags = {key: value[order] for key, value in source_flags.items()}

        tsrc = coords[:, 0].astype(np.int64)
        y = coords[:, 1].astype(np.int64)
        x = coords[:, 2].astype(np.int64)
        if self.cfg.same_timestep_queries:
            ttgt = tsrc.copy()
            tcam = tsrc.copy()
        else:
            ttgt = rng.integers(0, t, size=m, dtype=np.int64)
            tcam = ttgt.copy()

        u = x.astype(np.float32) / float(max(w - 1, 1))
        v = y.astype(np.float32) / float(max(h - 1, 1))
        dyn_labels = dynamic[tsrc, y, x].astype(np.float32)
        particle_labels = particle[tsrc, y, x].astype(np.float32)
        return {
            "u": u,
            "v": v,
            "t_src": tsrc,
            "t_tgt": ttgt,
            "t_cam": tcam,
            "dynamic_object": dyn_labels,
            "particle": particle_labels,
            "source_boundary": source_flags["boundary"],
            "source_small_particle": source_flags["small_particle"],
            "source_hard_static": source_flags["hard_static"],
            "source_dynamic": source_flags["dynamic"],
            "source_particle": source_flags["particle"],
            "source_background": source_flags["background"],
        }

    def __getitem__(self, index: int) -> dict[str, Any]:
        manifest_path, payload = self._clip_payload(index)
        rng = self._sample_rng(index=int(index), stream=23)
        video_rgb, dynamic, particle, aspect_ratio = self._load_clip(payload)
        query_pack = self._sample_queries(video_rgb=video_rgb, dynamic=dynamic, particle=particle, rng=rng)
        video = torch.from_numpy(video_rgb.astype(np.float32) / 255.0).permute(0, 3, 1, 2).contiguous()
        query = {
            "u": torch.from_numpy(query_pack["u"]).float(),
            "v": torch.from_numpy(query_pack["v"]).float(),
            "t_src": torch.from_numpy(query_pack["t_src"]).long(),
            "t_tgt": torch.from_numpy(query_pack["t_tgt"]).long(),
            "t_cam": torch.from_numpy(query_pack["t_cam"]).long(),
        }
        dyn = torch.from_numpy(query_pack["dynamic_object"]).float()
        particle_label = torch.from_numpy(query_pack["particle"]).float()
        transient_mask = torch.ones_like(dyn, dtype=torch.bool)
        return {
            "video": video,
            "aspect_ratio": torch.tensor([aspect_ratio], dtype=torch.float32),
            "query": query,
            "target": {
                "dynamic_object": dyn,
                "particle": particle_label,
            },
            "mask": {
                "transient": transient_mask,
            },
            "query_stats": {
                "synthetic_dynamic_object_query": dyn > 0.5,
                "synthetic_particle_query": particle_label > 0.5,
                "synthetic_boundary_query": torch.from_numpy(query_pack["source_boundary"]).bool(),
                "synthetic_small_particle_query": torch.from_numpy(query_pack["source_small_particle"]).bool(),
                "synthetic_static_hard_negative_query": torch.from_numpy(query_pack["source_hard_static"]).bool(),
                "synthetic_source_dynamic_query": torch.from_numpy(query_pack["source_dynamic"]).bool(),
                "synthetic_source_particle_query": torch.from_numpy(query_pack["source_particle"]).bool(),
                "synthetic_source_background_query": torch.from_numpy(query_pack["source_background"]).bool(),
            },
            "meta": {
                "manifest_path": str(manifest_path),
                "clip_name": str(payload.get("name", manifest_path.stem)),
            },
        }
